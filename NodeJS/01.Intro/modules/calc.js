module.exports = class Calc {

    addition (a, b) {
        return parseInt(a) + parseInt(b);
    }

    subtraction (a, b){
        return parseInt(a) - parseInt(b);
    }

    multiplication (a, b){
        return parseInt(a) * parseInt(b);
    }

    division (a, b){
        return parseInt(a) / parseInt(b);
    }
}
